package com.attendance.app.service;

/**
 * ログイン試行結果の種別を表す列挙型です。
 *
 * SecurityConfig におけるログイン失敗ハンドラや、リダイレクト先の切り替え判定に使用されます。
 */
public enum LoginAttemptResult {
    /**
     * 失敗回数が警告のしきい値未満（通常1〜2回）であることを示します。
     */
    NORMAL,

    /**
     * 失敗回数が警告のしきい値以上（通常3〜4回）であり、
     * アカウントが一時的（例：30分間）にロックされたことを示します。
     */
    TEMP_LOCKED,

    /**
     * 失敗回数がロックのしきい値以上（通常5回以上）であり、
     * アカウントが永続的にロックされたことを示します。
     * 管理者ユーザーは永久ロックの対象外のため、この結果を受け取ることはありません。
     */
    LOCKED
}
